package com.masqueprogramar;

/**
 * @author masqueprogramar (https://masqueprogramar.wordpress.com)
 * @date 30-diciembre-2016
 * @description Programa que muestra los límites de las clases numéricas utilizando las constantes MIN_VALUE y MAX_VALUE       
 * @version 1.0
 * @url https://masqueprogramar.wordpress.com/2016/12/30/limites-clases-numericas/ 
 */

public class LimitesClasesNumericas {
	
	public static void main(String[] args) {
        
        System.out.println("Límites de las clases númericas");
        System.out.println("*******************************");
        System.out.println("\nMínimo valor de byte    = " + Byte.MIN_VALUE);
        System.out.println("Máximo valor de byte    = " + Byte.MAX_VALUE);
        System.out.println("Mínimo valor de short   = " + Short.MIN_VALUE);
        System.out.println("Máximo valor de short   = " + Short.MAX_VALUE);
        System.out.println("Mínimo valor de int     = " + Integer.MIN_VALUE);
        System.out.println("Máximo valor de int     = " + Integer.MAX_VALUE);
        System.out.println("Mínimo valor de long    = " + Long.MIN_VALUE);
        System.out.println("Máximo valor de long    = " + Long.MAX_VALUE);
        System.out.println("Mínimo valor de float   = " + Float.MIN_VALUE);
        System.out.println("Máximo valor de float   = " + Float.MAX_VALUE);
        System.out.println("Mínimo valor de double  = " + Double.MIN_VALUE);
        System.out.println("Máximo valor de double  = " + Double.MAX_VALUE);
         
    }
}
